    <h5>1. Downloading the UDFs files</h5>
    <p>The UDFs files for Mathematical Methods and Specialist Mathematics are found in the 'Download' section of the Home page.</p>
    <p>Click on the files you wish to download, these should then be downloaded as zip folders onto your computer.</p>
    <img src = "/images/install-instruc-1.png" alt = "install-step-1" class = "step-img">
    <br>
    <h5>2. Extracting the UDF files</h5>
    <p>The zip-folder(s) should now be in the 'Downloads' section of your file explorer.</p>
    <img src = "/images/install-instruc-2.png" alt = "install-step-2" class = step-img>
    <p>Right click on the zip-file, then select the 'Extract All...' option</p>
    <img src = "/images/install-instruc-3.png" alt = "install-step-3" class = step-img>
    <p>A window displaying the path of where the files will be extracted to will now show up.</p>
    <p>Click on browse to change the path/location of where the files are extracted.</p>
    <img src = "/images/install-instruc-4.png" alt = "install-step-4" class = step-img>
    <p>Find the TiNspire CX folder on your computer, its location will depend on where you set it up.</p>
    <p>Afterwards, double click onto it to get into the folder.</p>
    <img src = "/images/install-instruc-5.png" alt = "install-step-5" class = step-img>
    <p>You should now see a folder called 'MyLib' inside the folder.</p>
    <p>Click onto the 'MyLib' folder then press 'select folder', then press 'extract' to move the folder into the 'MyLib' folder.</p>
    <img src = "/images/install-instruc-6.png" alt = "install-step-6" class = step-img>
    <p>A folder titled 'MM3&4_UDFs' should now be in your 'MyLib' folder.</p>
    <p>Now simply move the TiNspire files out of 'MM3&4_UDFs' and into 'MyLib'. You can then delete the 'MM3&4_UDFs' folder.</p>
    <p><b>Note:</b></p>
    <p>- If you downloaded for SM3&4 then repeat the same process.</p>
    <p>- Since the same EULA is in both files, if you downloaded both then you may get a notification asking you if you would like to replace 1 file. This is completely fine.</p>
    <p>- The 'MM3&4' TiNspire file is intended as a main/calculation document. Please move it out of 'MyLib'.</p>
    <h5>3. Transferring UDFs to the handheld CAS</h5>
    <p>There are two different methods. Either using the student software or using Ti-Nspire Connect (if your student software has expired).</p>
    <h6>Using the Student Software</h6>
    <p>Connect your handheld CAS to your computer using the black connection cabel.</p>
    <p>Your handheld should then appear at the bottom of the folder section.</p>
    <img src = "/images/install-instruc-8.png" alt = "install-step-8" class = step-img>
    <p>Click onto your CAS calculator to get into its files.</p>
    <p>Finally, drag your MyLib folder into your CAS calculator. This will transfer the UDFs files into your handheld CAS.</p>
    <img src = "/images/install-instruc-9.png" alt = "install-step-9" class = step-img>
    <h6>Using Ti-Nspire Connect</h6>
    <p>You can access Ti-Nspire Connect via this <a href="https://nspireconnect.ti.com/nsc/">link</a></p>
    <p>Connect your handheld CAS to your computer using the black connection cabel, then press 'Connect Calculator'.</p>
    <p>You will then be taken to a window where you will be prompted to select your calculator then press connect.</p>
    <img src = "/images/install-instruc-10.png" alt = "install-step-10" class = step-img>
    <p>You will then be taken to a window displaying multiple options, select 'Transfer File'</p>
    <img src = "/images/install-instruc-11.png" alt = "install-step-11" class = step-img>
    <p>Then select from computer to calculator.</p>
    <img src = "/images/install-instruc-12.png" alt = "install-step-12" class = step-img>
    <p>Afterwards select the 'from computer' option.</p>
    <img src = "/images/install-instruc-13.png" alt = "install-step-13" class = step-img>
    <p>You will then be taken to your file explorer, where you can select the TiNspire files you wish to transfer.</p>
    <img src = "/images/install-instruc-14.png" alt = "install-step-14" class = step-img>
    <p>Once you have transfered all the files, go onto your handheld then cut (Ctrl + X) and paste (Ctrl + V) the files into your MyLib folder.</p>
